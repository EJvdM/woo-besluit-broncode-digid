# This validator sets the confirmation error message not only on the attribute_confirmation field, but also
# on the associated attribute field.
class AssociatedConfirmationValidator < ActiveModel::Validations::ConfirmationValidator
  def validate_each(record, attribute, value)
    return unless (confirmed = record.send("#{attribute}_confirmation")) && (value != confirmed)

    record.errors.add(attribute, :confirmation)
    record.errors.add("#{attribute}_confirmation", :confirmation)
  end
end
