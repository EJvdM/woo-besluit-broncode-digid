module EmailConcern
  extend ActiveSupport::Concern

  def max_emails_per_day_error
    key = balie_or_svb_session? ? "email_max_verification_mails_on_request_abroad" : "email_max_verification_mails_on_request"
    url = balie_or_svb_session? ? "https://www.digid.nl" : check_email_url
    flash.now[:notice] = t(key, max_number_emails: ::Configuration.get_int("aantal_controle_emails_per_dag"), date: l(Time.zone.now.next_day, format: :date).strip)
    render_simple_message(ok: url)
  end
end
