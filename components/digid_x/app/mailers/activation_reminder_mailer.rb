class ActivationReminderMailer < ApplicationMailer

  # ED004
  def activation_reminder(kwargs = {})
    account_id = kwargs[:account_id]
    recipient = kwargs[:recipient]
    @deadline = kwargs[:deadline].to_date

    begin
      i18n_mail(account_id: account_id, to: recipient, subject: "email.subject.activation_reminder")
      Log.instrument("301", account_id: account_id)
      SentEmail.create(account_id: account_id, reason: ::SentEmail::Reason::REMINDER_ACTIVATION)
    rescue => e
      Log.instrument("302", account_id: account_id, reason: e.message)
    end
  end
end
