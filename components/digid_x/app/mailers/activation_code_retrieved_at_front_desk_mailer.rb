class ActivationCodeRetrievedAtFrontDeskMailer < ApplicationMailer
  # mail the user a confirmation email (EDB003)
  def activation_code_retrieved_at_front_desk(kwargs = {})
    account_id = kwargs[:account_id]
    recipient = kwargs[:recipient]

    begin
      i18n_mail(account_id: account_id, to: recipient, subject: "email.subject.activation_code_retrieved_at_front_desk")
    rescue
      # This is only called in environments for which raise_delivery_errors is enabled
      Log.instrument("525")
    else
      Log.instrument("524")
    end
    SentEmail.create(account_id: account_id, reason: ::SentEmail::Reason::SENT_EMAIL_BALIE_CODE)
  end
end
