# Use non abstract base to limit the connections to the logs database
class LogBase < ActiveRecord::Base
  # Set here the connection, so it is used in all subclasses
  establish_connection "#{Rails.env}_log".to_sym unless Rails.env.test?

  class << self
    # Set table_name to logs only if the class is LogsBase
    def table_name
      superclass == ActiveRecord::Base ? "logs" : super
    end

    # Modify base class to have correct naming for other classes
    def base_class
      if logs_db? && !superclass.logs_db?
        self
      else
        super
      end
    end

    def logs_db?
      self < LogBase
    end
  end
end
