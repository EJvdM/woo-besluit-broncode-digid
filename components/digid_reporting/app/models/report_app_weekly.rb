# Static frontend for weekly reports of ReportApp.
#
# Overrides reportname and report methods. Is called by
# AdminReport during the weekly report generation.
# When run manually takes a date as parameter.
class ReportAppWeekly < AdminReport
  # Return the report name as identifier in the DigiD admin application.
  def self.report_name
    'Overzicht gebruik DigiD app per week'
  end

  # Run the report for the last week, or for the week
  # including the date in the parameter.
  def self.report(start_date = nil)
    rep = ReportApp.new(ReportParam::WEEK)
    return rep.report(start_date)
  end
end
