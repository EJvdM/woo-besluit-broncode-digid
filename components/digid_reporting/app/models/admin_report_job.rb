# The job handler for admin reports
class AdminReportJob
  include Sidekiq::Worker
  sidekiq_options queue: 'reporting'

  def perform(report_type, report_date)
    report_date = Date.today unless report_date.present?
    case report_type
    when "fraud" then AdminReport.generate_fraud_reports(report_date)
    when "integrity" then AdminReport.generate_integrity_reports(report_date)
    when "weekly" then AdminReport.generate_weekly_reports(report_date)
    when "monthly" then AdminReport.generate_monthly_reports(report_date)
    end
  end
end
