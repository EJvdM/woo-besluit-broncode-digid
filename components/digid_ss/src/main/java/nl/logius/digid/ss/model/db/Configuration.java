package nl.logius.digid.ss.model.db;

import java.time.ZoneOffset;
import java.time.ZonedDateTime;
import java.util.Map;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "configurations")
public class Configuration {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String name;
    private String value;
    private String label;
    private int position;
    private ZonedDateTime createdAt;
    private ZonedDateTime updatedAt;
    private String defaultValue;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public int getPosition() {
        return position;
    }

    public void setPosition(int position) {
        this.position = position;
    }

    public ZonedDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(ZonedDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public ZonedDateTime getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(ZonedDateTime updatedAt) {
        this.updatedAt = updatedAt;
    }

    public String getDefaultValue() {
        return defaultValue;
    }

    public void setDefaultValue(String defaultValue) {
        this.defaultValue = defaultValue;
    }

    public void updateMap(Map<String, String> valuesMap) {
        setDates();
        if (valuesMap.containsKey("name")) {
            name = valuesMap.get("name");
        }
        if (valuesMap.containsKey("value")) {
           value = valuesMap.get("value");
        }
        if (valuesMap.containsKey("default_value")) {
            defaultValue = valuesMap.get("default_value");
        }
        if (valuesMap.containsKey("label")) {
            label = valuesMap.get("label");
        }
        if (valuesMap.containsKey("position")) {
            position = Integer.parseInt(valuesMap.get("position"));
        }

    }

    public void setDates() {
        if (createdAt == null) createdAt = ZonedDateTime.now(ZoneOffset.UTC);
        updatedAt = ZonedDateTime.now(ZoneOffset.UTC);
    }

}
