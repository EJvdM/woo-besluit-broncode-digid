package nl.logius.digid.logger.controllers;

import io.swagger.annotations.Api;
import io.swagger.annotations.ApiOperation;
import nl.logius.digid.logger.model.LogRequest;
import nl.logius.digid.logger.service.LogService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/iapi/logs")
@Api(value = "/iapi/logs", description = "logs for the digid applications")
public class LogController implements BaseController {
    private final LogService service;

    @Autowired
    public LogController(LogService service) {
        this.service = service;
    }

    @ApiOperation("create a log")
    @PostMapping(value = "", consumes = "application/json", produces = "application/json")
    public void create(@RequestBody LogRequest log) {
        service.publish(log);
    }
}
