package nl.logius.digid.app.domain.notification.flow.step;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

import java.time.ZonedDateTime;

@JsonIgnoreProperties(ignoreUnknown = true)
public class Notification {

    private Long id;
    private String title;
    private String content;
    private boolean statusRead = false;
    private ZonedDateTime createdAt;

    public Notification(){

    }

    public Notification(Long id, String title, String content, boolean statusRead, ZonedDateTime createdAt) {
        this.id = id;
        this.title = title;
        this.content = content;
        this.statusRead = statusRead;
        this.createdAt = createdAt;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public boolean isStatusRead() {
        return statusRead;
    }

    public void setStatusRead(boolean statusRead) {
        this.statusRead = statusRead;
    }

    public ZonedDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(ZonedDateTime createdAt) {
        this.createdAt = createdAt;
    }
}
