package nl.logius.digid.app.shared.request;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.databind.PropertyNamingStrategies;
import com.fasterxml.jackson.databind.annotation.JsonNaming;
import io.swagger.v3.oas.annotations.media.Schema;

/**
 * Empty class to support empty body in generic handling
 */
@JsonNaming(PropertyNamingStrategies.SnakeCaseStrategy.class)
public class AppRequest {
    @JsonIgnore
    private String ipAddress;

    public String getIpAddress() {
        return ipAddress;
    }

    public void setIpAddress(String ipAddress) {
        this.ipAddress = ipAddress;
    }

    // Default method to get userAppId from a request
    // returns null if it does not exist in the request, returns the value if it does exist
    @Schema(hidden = true)
    public String getUserAppId(){
        return null;
    }
}
