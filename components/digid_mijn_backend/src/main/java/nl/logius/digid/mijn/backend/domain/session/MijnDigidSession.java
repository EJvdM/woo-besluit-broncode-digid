package nl.logius.digid.mijn.backend.domain.session;

import java.io.Serializable;
import java.util.UUID;

import org.springframework.data.annotation.Id;
import org.springframework.data.redis.core.RedisHash;
import org.springframework.data.redis.core.TimeToLive;

@RedisHash(value= "MijnDigidSession", timeToLive = 20 * 60)
public class MijnDigidSession implements Serializable {

    public static final String MIJN_DIGID_SESSION_HEADER = "MIJN_DIGID_SESSION";

    private static final long serialVersionUID = 1;

    @Id
    private String id;

    private boolean authenticated;

    private Long accountId;

    private long createdAt;
    private String appCode;
    private String deviceName;

    @TimeToLive
    private long ttl;

    public MijnDigidSession(long ttl) {
        this.id = UUID.randomUUID().toString();
        this.createdAt = System.currentTimeMillis();
        this.ttl = ttl;
    }

    public String getId() {
        return id;
    }

    public Long getAccountId() {
        return accountId;
    }

    public void setAccountId(Long accountId) {
        this.accountId = accountId;
    }

    public boolean isAuthenticated() {
        return authenticated;
    }

    public void setAuthenticated(boolean authenticated) {
        this.authenticated = authenticated;
    }

    public String getAppCode() {
        return appCode;
    }

    public void setAppCode(String appCode) {
        this.appCode = appCode;
    }

    public String getDeviceName() {
        return deviceName;
    }

    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }
}
