package nl.logius.digid.ns.repository;

import nl.logius.digid.ns.model.AppNotification;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.transaction.annotation.Transactional;

import java.util.List;
import java.util.Optional;

public interface AppNotificationRepository extends JpaRepository<AppNotification, Long> {

    @Query(value = "SELECT * FROM app_notifications n " +
            "WHERE n.notification_status = :status AND " +
            "n.created_at > DATE_ADD(utc_timestamp(), INTERVAL :minutes MINUTE) AND " +
            "n.date_sent IS NULL", nativeQuery = true)
    Optional<List<AppNotification>> findAllAppNotificationsByStatusAndDateCreatedAndDateSent(@Param("status") String status, @Param("minutes") int minutes);

    @Transactional
    @Modifying
    @Query(value = "DELETE FROM app_notifications WHERE updated_at < DATE_SUB(NOW(),INTERVAL :months MONTH)", nativeQuery = true)
    void deleteByUpdatedAtWithRetentionPeriodMonths( @Param("months") int months);
}
