  # allowed hosts, needed for rails 6
  allowed_hosts = ["SSSSSSSSSSSSSSSS", "SSSSSSSSSSSSSSSSSSSSSS", "127.0.0.1", "localhost"]
  config.hosts += allowed_hosts
