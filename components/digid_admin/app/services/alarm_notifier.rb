require 'alarm_notifier/message'
require 'alarm_notifier/email'
require 'alarm_notifier/sms'
require 'alarm_notifier/report'
require 'alarm_notifier/admin_report'
require 'alarm_notifier/munin'

class AlarmNotifier
  def initialize(report_type, message)
    @report_type = report_type
    @message = Message.new(message)
  end

  def send_reports
    Email.new(@report_type, @message).send_reports
    Sms.new(@report_type, @message).send_reports
    Log.instrument('280', subject: @message.subject)
  end
end
