class AselectWebservice < AccountBase
  belongs_to :webservice

  has_many :shared_secrets
  accepts_nested_attributes_for :shared_secrets, allow_destroy: true


  has_many :certificates
  validates :app_id, uniqueness: { case_sensitive: true }, allow_blank: true
  validate :max_number_of_shared_secrets

  private

  def max_number_of_shared_secrets
    errors.add(:shared_secrets, I18n.t('shared_secrets.to_many_secrets')) if shared_secrets.reject(&:marked_for_destruction?).size > 2
  end
end
