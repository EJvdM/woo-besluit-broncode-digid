class WhitelistedPhoneNumber < AccountBase
  include FourEyes

  validates :phone_number, presence: true
  validates :description, presence: true

  validate :validate_format
  validate :phone_number_not_changed?

  def name
    phone_number
  end

  private

  def validate_format
    return if DigidUtils::PhoneNumber.valid_dutch_number?(phone_number)

    errors.add(:phone_number, "is niet geldig.")
  end

  def phone_number_not_changed?
    if phone_number_changed? && self.persisted?
      errors.add(:phone_number, "wijzigen is niet mogelijk.")
    end
  end
end
