module SearchEngineHelper
  private

  def elf_proef_helper(nummer)
    bsn = nummer.to_s.rjust(9, '0')
    last_pos = bsn.slice!(/.$/).to_i
    total = -last_pos
    bsn.reverse.chars.each_with_index { |digit, index| total += digit.to_i * (index + 2) }
    total % 11 == 0
  end
end
