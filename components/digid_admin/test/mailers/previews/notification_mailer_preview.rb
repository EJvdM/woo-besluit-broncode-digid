# Preview all emails at http://localhost:3000/rails/mailers/notification_mailer
class NotificationMailerPreview < ActionMailer::Preview
  include FactoryBot::Syntax::Methods

  def notify_block
    NotificationMailer.notify_block(account_id: account.id, doc_type: "driving_licence")
  end

  def notify_deblock
    NotificationMailer.notify_deblock(account_id: account.id, doc_type: "driving_licence")
  end

  def notify_pin
    NotificationMailer.notify_pin(account_id: account.id, doc_type: "driving_licence")
  end

  def account
    create(:account_with_email, locale: params[:locale])
  end
end
