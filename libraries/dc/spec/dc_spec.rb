RSpec.describe Dc do
  before(:each) do
    Dc.configure do |c|
      c.base_url = "http://something"
      c.token = "something"
    end
  end

  it "has a version number" do
    expect(Dc::VERSION).not_to be nil
  end

  it "can be configured correctly" do
    expect(Dc.configuration.base_url).to eq("http://something")
    expect(Dc.configuration.token).to eq("something")
  end
end
