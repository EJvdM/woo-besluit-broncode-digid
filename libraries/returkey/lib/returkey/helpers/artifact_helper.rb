module Returkey
  module Helpers
    module ArtifactHelper
      def root_url
        "http://localtest.me:#{request.port}"
      end

      def resolve_url
        root_url
      end

      def resolve_uri
        URI(resolve_url)
      end

      def resolve_artifact
        http = Net::HTTP.new(resolve_uri.host, resolve_uri.port)
        request = Net::HTTP::Post.new(resolve_uri.path)
        request.body = saml_artifact
        response = http.request(request)
        response.body
      end

      def saml_artifact
        Returkey::Saml::ArtifactResolve.new(request).as_signed_xml
      end
    end
  end
end
